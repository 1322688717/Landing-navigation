package com.example.try_68.Msg;

/**
 * 关于用户的实体类
 */
public class UserMsg {
    int ueserid;
    String username;
    String pwd;

    public int getUeserid() {
        return ueserid;
    }

    public void setUeserid(int ueserid) {
        this.ueserid = ueserid;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }
}
